// → Opérateurs arithmétiques

// +  : Addition
// -  : Soustraction
// *  : Multiplication
// /  : Division
// %  : Modulo (reste d'une division d'entiers)
// ** : Exposant

$res = 2 + 3; // 5
echo "<p>2 + 3 = " . $res . "</p>";
$res = 2 - 3; // -1
echo "<p>2 - 3 = " . $res . "</p>";
$res = 2 * 3; // 6
echo "<p>2 * 3 = " . $res . "</p>";
$res = 2 / 3; // 0.66
echo "<p>2 / 3 = " . $res . "</p>";
$res = 2 % 3; // 2
echo "<p>2 % 3 = " . $res . "</p>";
$res = 2 ** 3; // 8
echo "<p>2 ** 3 = " . $res . "</p>";